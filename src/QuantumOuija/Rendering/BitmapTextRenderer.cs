using Microsoft.Xna.Framework;
using Microsoft.Xna.Framework.Graphics;

namespace QuantumOuija.Rendering;

public sealed class BitmapTextRenderer
{
    private const int GlyphWidth = 5;
    private const int GlyphHeight = 7;
    private readonly Dictionary<char, string[]> _glyphs = CreateGlyphs();

    public void DrawText(
        SpriteBatch spriteBatch,
        Texture2D pixel,
        string text,
        Vector2 position,
        Color color,
        int scale,
        float maxWidth)
    {
        var cursor = position;
        var lineHeight = (GlyphHeight + 2) * scale;
        var charAdvance = (GlyphWidth + 1) * scale;
        var maxChars = Math.Max(1, (int)(maxWidth / charAdvance));
        var column = 0;

        foreach (var raw in text)
        {
            var character = char.ToUpperInvariant(raw);
            if (character == '\n' || column >= maxChars && character == ' ')
            {
                cursor.X = position.X;
                cursor.Y += lineHeight;
                column = 0;
                continue;
            }

            if (column >= maxChars)
            {
                cursor.X = position.X;
                cursor.Y += lineHeight;
                column = 0;
            }

            DrawGlyph(spriteBatch, pixel, character, cursor, color, scale);
            cursor.X += charAdvance;
            column++;
        }
    }

    private void DrawGlyph(SpriteBatch spriteBatch, Texture2D pixel, char character, Vector2 position, Color color, int scale)
    {
        if (character == ' ')
        {
            return;
        }

        if (!_glyphs.TryGetValue(character, out var rows))
        {
            rows = _glyphs['?'];
        }

        for (var y = 0; y < GlyphHeight; y++)
        {
            for (var x = 0; x < GlyphWidth; x++)
            {
                if (rows[y][x] != '1')
                {
                    continue;
                }

                spriteBatch.Draw(
                    pixel,
                    new Rectangle((int)position.X + x * scale, (int)position.Y + y * scale, scale, scale),
                    color);
            }
        }
    }

